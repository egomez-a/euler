#include <stdio.h>

int    main (void)
{
    int i;
    int n;

    i = 2;
    n = 0;
    while (i < 1000)
    {
        if (i % 3 == 0 || i % 5 == 0)
        {
            printf("  %d   --- ", i);
            n = n + i;
            printf("suma = %d\n", n);
        }
        i++;
    }
    return (n);
}